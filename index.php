<div class="index1">
Nous connaissons bien nos animaux de compagnie chiens. Ils ont tous un caractère bien marqué et c'est cela qui fait leur personnalité. 
Notre association <a class="alink" href="contactcm.php"  title="Situer ou contacter DogAnima">DogAnima</a> propose différentes formules de gardiennage. Ceux 
sont des véritables solutions alternatives à la pension pour chien.
</div>
<p></p>
<div class="index21">
<b>La garde en famille d’accueil</b>
<p></p>
Cette prestation est la plus demand&eacute;e pour faire garder son chien. Le jour de votre d&eacute;part en vacances, vous confiez votre compagnon à l’une 
de nos <a class="alink" href="tarifs.php"  title="Pet Sitter familles d'accueil">familles d’accueil</a> qui saura lui apporter toute l’attention dont il aura besoin.
La garde de chien en famille d’accueil est id&eacute;ale pour les chiens qui n’aiment pas la solitude et qui ont besoin d’attention.
<p></p>
<b>La garde à domicile</b>
<p></p>
Pour cette garde, votre chien ne se d&eacute;place pas car c’est la nounou qui prend place à votre domicile pour s’occuper de votre compagnon. Le 
temps de votre absence, le <a class="alink" href="tarifs.php"  title="Intervenants Pet Sitter">Pet Sitter</a> s’installe chez vous pour garder votre chien avec l’obligation de dormir à votre domicile.
</div>

<div class="index22">
Pendant la journée le Pet Sitter est libre de ses occupations (travail, sport ou autre) Le soir au lieu de retourner à son domicile, il se rend 
chez vous pour retrouver votre compagnon et s’occuper de lui jusqu’à votre retour.
<p></p>
La garde à domicile n'est pas du homesitting : les tâches m&eacute;nag&egrave;res ou autres activit&eacute;s du quotidien ne font pas partie des missions 
du Pet Sitter. La principale mission du Pet Sitter est de s’occuper du bien-être de votre animal jusqu’à votre retour : nourriture, eau, promenades, câlins, etc.
<p></p>
La garde de chien à domicile est id&eacute;ale pour les chiens qui n’aiment pas le changement d’environnement et qui sont habitu&eacute;s à leur confort.
</div>
