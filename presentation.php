<b>L'association DogAnima</b>
<p></p>
A l'approche des vacances la garde de nos chiens est souvent un sujet d'inquiétude.
<p></p>
Les pensions traditionnelles ne nous offrent pas toujours un service à la hauteur de nos attentes et sont souvent surchargées durant les périodes estivales.
<p></p>
C'est en partant du constat que cette situation favorisait les abandons et la détresse des animaux que l'association <a class="alink" href="contactcm.php"  title="Contacter DogAnima">DogAnima</a> a vu le jour début 2015.
<p></p>
Présente sur toute l'Indre et Loire, l'association DogAnima est l'une des association leader pour la garde de chien en familles d'accueil et de nounous à 
domicile. Issue de la collaboration de plusieurs spécialistes du monde animal, vétérinaires, éleveurs et dogsitters, DogAnima vous propose aujourd'hui un vaste 
réseau de familles d'accueil fiables et aimantes des chiens sur tout l'Indre et Loire. <a class="alink" href="tarifs.html"  title="Intervenants DogAnima">Les nounous à domicile</a> on tous une expérience positive de garde de chien.
<p></p> 
</div>
<div class="present12">
<b>Le concept</b>
<p></p>
Nos familles d'accueil et nos nounous à domicile permettent à vos chiens de rester dans un cadre familial, entourés d'affection. 
Les chiens sont chouchoutés et considérés, en accueil, comme un nouveau membre de la famille. Ils bénéficient d'une attention toute particulière, 
semblable à celle que vous pouvez leur apporter.
